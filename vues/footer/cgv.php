  <h2>Conditions Générales de ventes</h2>
  <h3>Article 1</h3>
  <p>Sont exclues les manifestations à caractère politique, religieux ou militant ainsi que toutes manifestations susceptibles de provoquer controverses ou troubles publics.</p>
  <h3>Article 2</h3>
  <p>Les tarifs et les conditions de location sont ceux stipulés dans le devis dûment signé par le preneur et les conditions générales de vente.</p>
  <h3>Article 3</h3>
  <p>La durée de la manifestation sera conforme aux horaires convenus au moment de la réservation. La prise de possession et la libération de l’espace loué devra intervenir dans le créneau horaire convenu.</p>
  <h3>Article 4</h3>
  <p>La reprise de votre matériel doit s’effectuer immédiatement après son utilisation.</p>
  <h3>Article 5</h3>
  <p>Conformément au décret n° 92-478 du 29 mai 1992 et la loi du 1er janvier 2008, nous rappelons qu’il est interdit de fumer dans les lieux affectés à un usage collectif.</p>
  <h3>Article 6</h3>
  <p>Une réservation sera considérée comme ferme et définitive par retours du devis signé et revêtu de la mention « Bon pour accord », ainsi que la réception d’un acompte de 50% du montant de location de la salle.</p>
  <h3>Article 7</h3>
  <p>En cas d’annulation et quelle qu’en soit la cause, LokiSalle conservera les frais d’annulation.</p>
  <h3>Article 8</h3>
  <p>En cas de renonciation du fait du loueur, lié à des impossibilités techniques ou en cas de force majeure, l’indemnisation ne pourra être supérieure aux sommes versées.</p>
  <h3>Article 9</h3>
  <p>Le nombre de personnes indiqué pour chacune des salles ne doit pas être dépassé pour motif de sécurité.</p>
  <h3>Article 10</h3>
  <p>La salle mise à la disposition devra être restituée dans un état correct d’utilisation. En cas de dégradations commises par le client, les réparations seront évaluées par procès-verbal et seront suivies d’une indemnisation par le client, sans délai de recours aux assurances.</p>
  <h3>Article 11</h3>
  <p>Cette acceptation est liée à la signature du bon de réservation.</p>
  <h3>Article 12</h3>
  <p>Toute contestation relative aux présentes prestations relève de la seule compétence des tribunaux du ressort de la Cour d’appel de Paris.</p>
